def tensorFromSentence(sentence):
    return torch.tensor(sentence+[EOS_token], dtype=torch.long, device=device).view(-1, 1)


def tensorFromPairs(pairs):
    input_tensor = tensorFromSentence(pairs[0])
    target_tensor = tensorFromSentence(pairs[1])
    return (input_tensor, target_tensor)

